n = input('Enter: ')
for i in range(1, 6):
    print ('*' * n)

for i in range (n,0,-1):
     print((n-i) * ' ' + i * '*')

for i in range(n):
    print ('*' * (i+1))

for i in range(n):
    print ((n-i) * '*')

for i in range (n,0,-1):
     print((i) * ' ' + (n+1-i) * '*')






























































